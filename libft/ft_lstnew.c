#include "libft.h"

t_stack	*ft_lstnew(int data)
{
	t_stack	*new_node;

	new_node = (t_stack *)malloc(sizeof(t_stack));
	if (new_node == NULL)
		return (NULL);
	new_node -> data = data;
	new_node -> next = NULL;
	return (new_node);
}
